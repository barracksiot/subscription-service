package io.barracks.subscriptionservice.rest;

import com.cheddargetter.model.Plans;
import io.barracks.subscriptionservice.manager.PlansManager;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.runners.MockitoJUnitRunner;

import static org.assertj.core.api.Assertions.assertThat;
import static org.mockito.Mockito.verify;
import static org.mockito.Mockito.when;

@RunWith(MockitoJUnitRunner.class)
public class PlansResourceTest {
    @Mock
    private PlansManager manager;

    @InjectMocks
    private PlansResource plansResource;


    @Test
    public void getPlans_whenSucceeds_shouldReturnPlans() throws Exception {
        // Given
        final Plans expected = new Plans();
        when(manager.getPlans()).thenReturn(expected);

        // When
        final Plans result = plansResource.getPlans();

        // Then
        assertThat(result).isEqualTo(expected);
        verify(manager).getPlans();
    }
}
